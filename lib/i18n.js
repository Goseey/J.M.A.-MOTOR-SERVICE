/** Safe deep-getter for dot-paths like 'nav.home'. */
function getPath(obj, path) {
  return path.split('.').reduce((o, k) => (o == null ? undefined : o[k]), obj);
}

/** Build a translator bound to a language. Falls back to English, then to the key. */
export function makeT(lang) {
  return function t(key, fallback) {
    const v = getPath(TRANSLATIONS[lang] || TRANSLATIONS.en, key);
    if (v !== undefined && v !== null) return v;
    const fb = getPath(TRANSLATIONS.en, key);
    if (fb !== undefined && fb !== null) return fb;
    return fallback ?? key;
  };
}
